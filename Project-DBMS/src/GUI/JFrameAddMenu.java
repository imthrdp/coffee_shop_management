    private void jButtonAddMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddMenuActionPerformed
        // BUTTION Đăng ký tài khoản đăng nhập mới.
        // Kiểm tra món đã tồn tại chưa:
        boolean isExist = false;
        try {
            pst = conn.prepareStatement("SELECT * FROM quanlyquancafethucan.menu WHERE TenMon = ?");
            pst.setString(1, jTextFieldAddMenuFoodName.getText());
            rs = pst.executeQuery();
            if(rs.next()) {
                JOptionPane.showMessageDialog(null, "Món này hiện đã tồn tại!", "Món đã tồn tại.", 2);
                isExist = true;     // Đã tồn tại.
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "JFrameAddMenu.", 2);
        }
        // Nếu chưa thì thực hiện thêm món vào:
        if(isExist == false) {
            try {
                cst = conn.prepareCall("{CALL ThemMenu(?, ?, ?, ?, ?)}");
                cst.setInt(1, (int) jSpinnerAddMenuID.getValue());
                if(jComboBoxAddMenuCategory.getSelectedItem().toString().equals("Nước uống")) {
                    cst.setInt(2, 1);   // ID Phân loại. 1 || 2 || 3
                }
                else if(jComboBoxAddMenuCategory.getSelectedItem().toString().equals("Thức ăn")) {
                    cst.setInt(2, 2);   // ID Phân loại. 1 || 2 || 3
                }
                else cst.setInt(2, 3);  // ID Phân loại. 1 || 2 || 3
                cst.setString(3, jTextFieldAddMenuFoodName.getText());          // Tên món
                cst.setString(4, jComboBoxAddMenuCategory.getSelectedItem().toString());          // Phân loại Thức ăn || Nước uống || Khác
                cst.setDouble(5, (double) jSpinnerAddMenuPrice.getValue());   // Giá
                cst.executeQuery();
                Table.LoadDataTable("SELECT ID AS 'Mã món', TenMon AS 'Tên món', PhanLoai AS 'Phân loại', Gia AS 'Giá bán' FROM quanlyquancafethucan.menu ORDER BY ID ASC", JFrameMain.Admin.jTableMenu);
                JOptionPane.showMessageDialog(null, "Đã thêm " + jTextFieldAddMenuFoodName.getText() + " vào Menu!", "Thêm món.", 1);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Mã Món không được trùng nhau!", "Thông báo lỗi.", 2);
            }
        }
    }//GEN-LAST:event_jButtonAddMenuActionPerformed
    
