    private int getMenuRowCount() {
        int menuRowCount = 0;
        try {
            pst = conn.prepareStatement("SELECT * FROM menu");
            rs = pst.executeQuery();
            while (rs.next()) menuRowCount = rs.getRow();
        } catch (Exception e) {
        }
        return menuRowCount;
    }
    
